public class OperadoresDeComparacion {
    
    public static void main(String[] args){

        double sueldo1 = 10000.5322;
        double sueldo2 = 43223.2311;

        System.out.println(sueldo1>sueldo2);
        System.out.println(sueldo1<sueldo2);
        System.out.println(sueldo1>=sueldo2);
        System.out.println(sueldo1<=sueldo2);
        System.out.println(sueldo1!=sueldo2);
        System.out.println(sueldo1==sueldo2);

    }

}
